package gen

import (
	"fmt"
	"path/filepath"
	"strings"
	"unicode"

	"github.com/flunderpero/metall/metallc/internal/ast"
	"github.com/flunderpero/metall/metallc/internal/base"
	"github.com/flunderpero/metall/metallc/internal/types"
)

func GenCHeader(a *ast.AST, module ast.Module, exports []types.ExportWork) string {
	displayName := filepath.Base(module.FileName)
	guard := headerGuard(displayName)
	var sb strings.Builder
	fmt.Fprintf(&sb, "// Generated by metallc from %s.\n", displayName)
	fmt.Fprintf(&sb, "// Do not edit by hand.\n")
	fmt.Fprintf(&sb, "#ifndef %s\n", guard)
	fmt.Fprintf(&sb, "#define %s\n\n", guard)
	sb.WriteString("#include <stdbool.h>\n")
	sb.WriteString("#include <stdint.h>\n\n")
	sb.WriteString("#ifdef __cplusplus\n")
	sb.WriteString("extern \"C\" {\n")
	sb.WriteString("#endif\n\n")
	for _, exp := range exports {
		funType := base.Cast[types.FunType](exp.Env.Type(exp.FunTypeID).Kind)
		targetFun := base.Cast[ast.Fun](a.Node(exp.FunNodeID).Kind)
		retC := cType(exp.Env, funType.Return)
		fmt.Fprintf(&sb, "%s %s(", retC, exp.CName)
		if len(funType.Params) == 0 {
			sb.WriteString("void")
		} else {
			for i, paramTypeID := range funType.Params {
				if i > 0 {
					sb.WriteString(", ")
				}
				paramC := cType(exp.Env, paramTypeID)
				paramNode := base.Cast[ast.FunParam](a.Node(targetFun.Params[i]).Kind)
				fmt.Fprintf(&sb, "%s %s", paramC, paramNode.Name.Name)
			}
		}
		sb.WriteString(");\n")
	}
	sb.WriteString("\n#ifdef __cplusplus\n")
	sb.WriteString("}\n")
	sb.WriteString("#endif\n\n")
	fmt.Fprintf(&sb, "#endif // %s\n", guard)
	return sb.String()
}

func cType(env *types.TypeEnv, typeID types.TypeID) string {
	typ := env.Type(typeID)
	switch kind := typ.Kind.(type) {
	case types.VoidType:
		return "void"
	case types.BoolType:
		return "bool"
	case types.IntType:
		return cIntTypeName(kind)
	}
	panic(base.Errorf("cType: %s is not C-exportable", env.TypeDisplay(typeID)))
}

func headerGuard(fileName string) string {
	var sb strings.Builder
	for _, r := range strings.ToUpper(fileName) {
		if unicode.IsLetter(r) || unicode.IsDigit(r) {
			sb.WriteRune(r)
		} else {
			sb.WriteRune('_')
		}
	}
	s := strings.Trim(sb.String(), "_")
	if s == "" {
		return "METALL_EXPORT_H"
	}
	if unicode.IsDigit(rune(s[0])) {
		s = "_" + s
	}
	return s + "_H"
}

func cIntTypeName(t types.IntType) string {
	switch t.Name {
	case "I8":
		return "int8_t"
	case "I16":
		return "int16_t"
	case "I32":
		return "int32_t"
	case "Int":
		return "int64_t"
	case "U8":
		return "uint8_t"
	case "U16":
		return "uint16_t"
	case "U32":
		return "uint32_t"
	case "U64":
		return "uint64_t"
	case "Rune":
		return "uint32_t"
	}
	panic(base.Errorf("cIntTypeName: unknown int type %q", t.Name))
}
