package gen

import (
	"fmt"
	"strings"

	"github.com/flunderpero/metall/metallc/internal/ast"
	"github.com/flunderpero/metall/metallc/internal/base"
	"github.com/flunderpero/metall/metallc/internal/types"
)

type CodeWriter struct {
	indent int
	sb     strings.Builder
}

func NewCodeWriter() *CodeWriter {
	return &CodeWriter{indent: 0, sb: strings.Builder{}}
}

func (g *CodeWriter) write(args ...any) {
	if len(args) == 0 {
		return
	}
	indent := strings.Repeat("    ", g.indent)
	g.sb.WriteString(indent)
	arg0 := base.Cast[string](args[0])
	arg0 = strings.ReplaceAll(arg0, "\n", "\n"+indent)
	if len(args) > 1 {
		fmt.Fprintf(&g.sb, arg0, args[1:]...)
	} else {
		g.sb.WriteString(arg0)
	}
	g.sb.WriteString("\n")
}

type Label string

type Symbol struct {
	Name string
	Reg  string
	Type string
}

type IRGen struct {
	CodeWriter
	engine       *types.Engine
	symbols      map[types.ScopeID]map[string]Symbol
	regCounter   int
	constCounter int
	strConsts    map[string]int
	astCode      map[ast.NodeID]string
	lastLabel    Label
}

func NewIRGen(engine *types.Engine) *IRGen {
	return &IRGen{
		CodeWriter:   *NewCodeWriter(),
		engine:       engine,
		symbols:      map[types.ScopeID]map[string]Symbol{},
		regCounter:   1,
		constCounter: 0,
		strConsts:    map[string]int{},
		astCode:      map[ast.NodeID]string{},
		lastLabel:    "",
	}
}

func (g *IRGen) Gen(id ast.NodeID) {
	node := g.engine.Node(id)
	switch kind := node.Kind.(type) {
	case ast.Assign:
		g.genAssign(id, kind)
	case ast.Block:
		g.genBlock(id, kind)
	case ast.Call:
		g.genCall(id, kind)
	case ast.Deref:
		g.genDeref(id, kind)
	case ast.If:
		g.genIf(id, kind)
	case ast.File:
		g.genFile(kind, node.Span)
	case ast.Fun:
		g.genFun(id, kind)
	case ast.Struct:
		g.genStruct(id, kind)
	case ast.StructLiteral:
		g.genStructLiteral(id, kind)
	case ast.FieldAccess:
		g.genFieldAccess(id, kind)
	case ast.FunParam:
		// Handled inline in genFun
	case ast.Ident:
		g.genIdent(id, kind)
	case ast.Int:
		g.genInt(id, kind)
	case ast.Bool:
		g.genBool(id, kind)
	case ast.String:
		g.genString(id, kind)
	case ast.Var:
		g.genVar(id, kind)
	case ast.SimpleType:
		// Types don't generate code
	case ast.Ref:
		g.genRef(id, kind)
	case ast.RefType:
		// Types don't generate code
	default:
		panic(base.Errorf("unknown node kind: %T", kind))
	}
}

func (g *IRGen) genFile(file ast.File, span base.Span) {
	g.write("; Generated by metallc")
	g.write("")
	g.write(`source_filename = "%s"`, span.Source.Name)
	g.write("")
	for _, decl := range file.Decls {
		g.Gen(decl)
	}
	g.write("; Global constants.")
	g.write("")
	consts := make([]string, len(g.strConsts))
	for value, id := range g.strConsts {
		consts[id] = value
	}
	for id, value := range consts {
		g.write(`@%d = private constant [%d x i8] c"%s"`, id, len(value)+1, value+`\00`)
	}
}

func (g *IRGen) genStruct(id ast.NodeID, astStruct ast.Struct) {
	typ := g.engine.TypeOfNode(id)
	g.write("%%%s = type { ; %s", typ.ID, astStruct.Name.Name)
	g.indent++
	for i, astFieldID := range astStruct.Fields {
		astField := base.Cast[ast.StructField](g.engine.Node(astFieldID).Kind)
		typ := g.irTypeOfNode(astFieldID)
		comma := ""
		if i < len(astStruct.Fields)-1 {
			comma = ","
		}
		g.write("%s%s ; %s", typ, comma, astField.Name.Name)
	}
	g.indent--
	g.write("}")
}

func (g *IRGen) genStructLiteral(id ast.NodeID, lit ast.StructLiteral) {
	g.Gen(lit.Target)
	for _, arg := range lit.Args {
		g.Gen(arg)
	}
	structType := g.engine.TypeOfNode(lit.Target)
	sb := strings.Builder{}
	reg := g.reg()
	fmt.Fprintf(&sb, "%s = alloca %%%s\n", reg, structType.ID)
	for i, arg := range lit.Args {
		fieldReg := g.reg()
		fmt.Fprintf(
			&sb,
			"%s = getelementptr %%%s, %%%s* %s, i32 0, i32 %d\n",
			fieldReg,
			structType.ID,
			structType.ID,
			reg,
			i,
		)
		fieldTyp := g.irTypeOfNode(arg)
		fmt.Fprintf(&sb, "store %s %s, ptr %s\n", fieldTyp, g.lookupCode(arg), fieldReg)
	}
	g.write(sb.String())
	g.setCode(id, reg)
}

func (g *IRGen) genFieldAccess(id ast.NodeID, fieldAccess ast.FieldAccess) {
	fieldType, ptrReg := g.genFieldAccessPtr(fieldAccess)
	reg := g.reg()
	g.write("%s = load %s, ptr %s", reg, fieldType, ptrReg)
	g.setCode(id, reg)
}

func (g *IRGen) genFieldAccessPtr(fieldAccess ast.FieldAccess) (fieldType string, ptrReg string) {
	g.Gen(fieldAccess.Target)
	targetType := g.engine.TypeOfNode(fieldAccess.Target)
	structType := base.Cast[types.StructType](targetType.Kind)
	fieldIndex := indexOfStructField(structType, fieldAccess.Field.Name)
	fieldType = g.irType(structType.Fields[fieldIndex].Type)
	ptrReg = g.reg()
	g.write(
		"%s = getelementptr %%%s, %%%s* %s, i32 0, i32 %d",
		ptrReg,
		targetType.ID,
		targetType.ID,
		g.lookupCode(fieldAccess.Target),
		fieldIndex,
	)
	return fieldType, ptrReg
}

func (g *IRGen) genFun(id ast.NodeID, astFun ast.Fun) {
	typ := g.engine.TypeOfNode(id)
	fun, ok := typ.Kind.(types.FunType)
	if !ok {
		panic(base.Errorf("expected fun type, got %T", typ.Kind))
	}
	name := astFun.Name.Name
	isMain := name == "main"
	ret := g.irType(fun.Return)
	if isMain {
		ret = "i32"
	}
	params := strings.Builder{}
	paramAllocas := strings.Builder{}
	for i, paramID := range astFun.Params {
		paramNode := g.engine.Node(paramID)
		param, ok := paramNode.Kind.(ast.FunParam)
		if !ok {
			panic(base.Errorf("expected fun param, got %T", paramNode.Kind))
		}
		if i > 0 {
			params.WriteString(", ")
		}
		g.Gen(paramID)
		preg := g.reg()
		typ := g.irTypeOfNode(paramID)
		params.WriteString(typ)
		params.WriteString(" ")
		params.WriteString(preg)
		areg := g.reg()
		fmt.Fprintf(&paramAllocas, "%s = alloca %s\n", areg, typ)
		fmt.Fprintf(&paramAllocas, "store %s %s, ptr %s", typ, preg, areg)
		g.setSymbol(paramID, param.Name.Name, areg, typ)
	}
	g.write("define %s @%s(%s) {", ret, name, params.String())
	if len(astFun.Params) > 0 {
		g.write(paramAllocas.String())
	}
	g.indent++
	g.Gen(astFun.Block)
	if isMain {
		g.write("ret i32 0")
	} else {
		lastCode := g.lookupCode(astFun.Block)
		retType := g.irType(fun.Return)
		if retType == "void" {
			g.write("ret void")
		} else {
			g.write("ret %s %s", retType, lastCode)
		}
	}
	g.indent--
	g.write("}\n")
}

func (g *IRGen) genBlock(id ast.NodeID, block ast.Block) {
	for _, expr := range block.Exprs {
		g.Gen(expr)
	}
	code := "void"
	if len(block.Exprs) > 0 {
		code = g.lookupCode(block.Exprs[len(block.Exprs)-1])
	}
	g.setCode(id, code)
}

func (g *IRGen) genIf(id ast.NodeID, ifNode ast.If) {
	g.Gen(ifNode.Cond)
	cond := g.lookupCode(ifNode.Cond)
	thenLabel := g.label("then", id)
	contLabel := g.label("endif", id)
	elseLabel := contLabel
	if ifNode.Else != nil {
		elseLabel = g.label("else", id)
	}
	g.write("br i1 %s, label %%%s, label %%%s", cond, thenLabel, elseLabel)
	g.writeLabel(thenLabel)
	g.Gen(ifNode.Then)
	phiThenLabel := g.lastLabel
	g.write("br label %%%s", contLabel)
	if ifNode.Else != nil {
		g.writeLabel(elseLabel)
		g.Gen(*ifNode.Else)
		g.write("br label %%%s", contLabel)
	}
	phiElseLabel := g.lastLabel
	g.writeLabel(contLabel)
	code := "void"
	if ifNode.Else != nil {
		phi := g.reg()
		thenCode := g.lookupCode(ifNode.Then)
		elseCode := g.lookupCode(*ifNode.Else)
		typ := g.irTypeOfNode(ifNode.Then)
		if typ != "void" {
			g.write(
				"%s = phi %s [%s, %%%s], [%s, %%%s]",
				phi,
				typ,
				thenCode,
				phiThenLabel,
				elseCode,
				phiElseLabel,
			)
			code = phi
		}
	}
	g.setCode(id, code)
}

func (g *IRGen) label(name string, id ast.NodeID) Label {
	return Label(fmt.Sprintf("%s_%s", name, id))
}

func (g *IRGen) writeLabel(label Label) {
	g.lastLabel = label
	g.write("%s:", label)
}

func (g *IRGen) genAssign(id ast.NodeID, assign ast.Assign) {
	g.Gen(assign.RHS)
	rhs := g.lookupCode(assign.RHS)
	lhsNode := g.engine.Node(assign.LHS)
	switch lhsKind := lhsNode.Kind.(type) {
	case ast.Ident:
		g.Gen(assign.LHS)
		symbol, ok := g.lookupSymbol(assign.LHS, lhsKind.Name)
		if !ok {
			panic(base.Errorf("assign to unknown variable: %s", lhsKind.Name))
		}
		g.write("store %s %s, ptr %s", symbol.Type, rhs, symbol.Reg)
	case ast.FieldAccess:
		fieldType, ptrReg := g.genFieldAccessPtr(lhsKind)
		g.write("store %s %s, ptr %s", fieldType, rhs, ptrReg)
	case ast.Deref:
		g.Gen(assign.LHS)
		ptr := g.lookupCode(lhsKind.Expr)
		g.write("store %s %s, ptr %s", g.irTypeOfNode(assign.LHS), rhs, ptr)
	default:
		panic(base.Errorf("assign to unknown expression: %T", lhsKind))
	}
	g.setCode(id, "void")
}

func (g *IRGen) genCall(id ast.NodeID, call ast.Call) {
	g.Gen(call.Callee)
	for _, arg := range call.Args {
		g.Gen(arg)
	}
	calleeType := g.engine.TypeOfNode(call.Callee)
	fun, ok := calleeType.Kind.(types.FunType)
	if !ok {
		panic(base.Errorf("callee is not a function"))
	}
	sb := strings.Builder{}
	retType := g.engine.Type(fun.Return)
	if builtin, ok := retType.Kind.(types.BuiltInType); ok && builtin.Name == "void" {
		g.setCode(id, "void")
	} else {
		reg := g.reg()
		sb.WriteString(reg + " = ")
		g.setCode(id, reg)
	}
	sb.WriteString("call ")
	sb.WriteString(g.irType(fun.Return))
	sb.WriteString(" @")
	sb.WriteString(g.lookupCode(call.Callee))
	sb.WriteString(" (")
	for i, arg := range call.Args {
		if i > 0 {
			sb.WriteString(", ")
		}
		sb.WriteString(g.irTypeOfNode(arg))
		sb.WriteString(" ")
		sb.WriteString(g.lookupCode(arg))
	}
	sb.WriteString(")")
	g.write(sb.String())
}

func (g *IRGen) genIdent(id ast.NodeID, ident ast.Ident) {
	if symbol, ok := g.lookupSymbol(id, ident.Name); ok {
		ptrreg := g.reg()
		g.write("%s = load %s, ptr %s", ptrreg, symbol.Type, symbol.Reg)
		g.setCode(id, ptrreg)
		return
	}
	g.setCode(id, ident.Name)
}

func (g *IRGen) genInt(id ast.NodeID, int_ ast.Int) {
	g.setCode(id, "%d", int_.Value)
}

func (g *IRGen) genBool(id ast.NodeID, bool_ ast.Bool) {
	v := 0
	if bool_.Value {
		v = 1
	}
	g.setCode(id, "%d", v)
}

func (g *IRGen) genString(id ast.NodeID, str ast.String) {
	cid, ok := g.strConsts[str.Value]
	if !ok {
		cid = g.constCounter
		g.constCounter++
		g.strConsts[str.Value] = cid
	}
	reg := g.reg()
	g.write("%s = getelementptr [%d x i8], ptr @%d, i32 0, i32 0", reg, len(str.Value)+1, cid)
	g.setCode(id, reg)
}

func (g *IRGen) genRef(id ast.NodeID, ref ast.Ref) {
	if symbol, ok := g.lookupSymbol(id, ref.Name.Name); ok {
		g.setCode(id, symbol.Reg)
		return
	}
	g.setCode(id, "ptr %s", ref.Name.Name)
}

func (g *IRGen) genDeref(id ast.NodeID, deref ast.Deref) {
	g.Gen(deref.Expr)
	exprType := g.engine.TypeOfNode(deref.Expr)
	ref, ok := exprType.Kind.(types.RefType)
	if !ok {
		panic(base.Errorf("dereference: expected reference, got %T", exprType.Kind))
	}
	code := g.lookupCode(deref.Expr)
	reg := g.reg()
	g.write("%s = load %s, ptr %s", reg, g.irType(ref.Type), code)
	g.setCode(id, reg)
}

func (g *IRGen) genVar(id ast.NodeID, v ast.Var) {
	g.Gen(v.Expr)
	// We are a bit lazy here and just use an `alloca` instruction.
	// LLVM will optimize this.
	reg := g.reg()
	typ := g.irTypeOfNode(v.Expr)
	expr := g.lookupCode(v.Expr)
	g.write("%s = alloca %s", reg, typ)
	g.write("store %s %s, ptr %s", typ, expr, reg)
	g.setCode(id, reg)
	g.setSymbol(id, v.Name.Name, reg, typ)
}

func (g *IRGen) reg() string {
	id := g.regCounter
	g.regCounter++
	return fmt.Sprintf("%%r%d", id)
}

func (g *IRGen) irTypeOfNode(nodeID ast.NodeID) string {
	typ := g.engine.TypeOfNode(nodeID)
	return g.irType(typ.ID)
}

func (g *IRGen) irType(typeID types.TypeID) string {
	typ := g.engine.Type(typeID)
	switch kind := typ.Kind.(type) {
	case types.BuiltInType:
		switch kind.Name {
		case "Bool":
			return "i1"
		case "Int":
			return "i64"
		case "Str":
			return "ptr"
		case "void":
			return "void"
		default:
			panic(base.Errorf("unknown builtin type: %s", kind.Name))
		}
	case types.RefType, types.StructType:
		return "ptr"
	case types.FunType:
		panic("unexpected fun type")
	default:
		panic(base.Errorf("unknown type kind: %T", typ.Kind))
	}
}

func (g *IRGen) setCode(astID ast.NodeID, code string, args ...any) {
	if _, ok := g.astCode[astID]; ok {
		panic(base.Errorf("code already set for %s", g.engine.AST.Debug(astID, false, 0)))
	}
	if len(args) > 0 {
		code = fmt.Sprintf(code, args...)
	}
	g.astCode[astID] = code
}

func (g *IRGen) lookupCode(astID ast.NodeID) string {
	code, ok := g.astCode[astID]
	if !ok {
		panic(base.Errorf("no reg for %s", g.engine.AST.Debug(astID, false, 0)))
	}
	return code
}

func (g *IRGen) setSymbol(nodeID ast.NodeID, name string, reg string, typ string) {
	scope := g.engine.ScopeGraph.NodeScope(nodeID)
	if g.symbols[scope.ID] == nil {
		g.symbols[scope.ID] = map[string]Symbol{}
	}
	if _, ok := g.symbols[scope.ID][name]; ok {
		panic(base.Errorf("symbol %s already defined in scope %d", name, scope.ID))
	}
	g.symbols[scope.ID][name] = Symbol{Name: name, Reg: reg, Type: typ}
}

func (g *IRGen) lookupSymbol(nodeID ast.NodeID, name string) (Symbol, bool) {
	scope := g.engine.ScopeGraph.NodeScope(nodeID)
	for scope != nil {
		if symbols, ok := g.symbols[scope.ID]; ok {
			if symbol, ok := symbols[name]; ok {
				return symbol, true
			}
		}
		scope = scope.Parent
	}
	return Symbol{}, false
}

func indexOfStructField(s types.StructType, name string) int {
	for i, field := range s.Fields {
		if field.Name == name {
			return i
		}
	}
	panic(base.Errorf("field %q not found in struct %q", name, s.Name))
}

func GenIR(fileID ast.NodeID, engine *types.Engine) (string, error) {
	g := NewIRGen(engine)
	g.Gen(fileID)
	g.write(builtins)
	return g.sb.String(), nil
}

const builtins = `
; >>> Runtime

;     External functions.
declare i32 @puts(ptr)
declare i32 @printf(ptr, ...)

;      Builtin functions.
define void @print_str(ptr %s) {
    call i32 @puts(ptr %s)
    ret void
}

@fmt_int = private constant [4 x i8] c"%d\0A\00"
define void @print_int(i64 %n) {
    call i32 (ptr, ...) @printf(ptr @fmt_int, i64 %n)
    ret void
}

@str_true = private constant [5 x i8] c"true\00"
@str_false = private constant [6 x i8] c"false\00"
define void @print_bool(i1 %n) {
	br i1 %n, label %true, label %false
	true:
	    call i32 @puts(ptr @str_true)
	    ret void
	false:
		call i32 @puts(ptr @str_false)
		ret void
}
`
