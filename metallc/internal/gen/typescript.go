package gen

import (
	"fmt"
	"path/filepath"
	"strings"

	"github.com/flunderpero/metall/metallc/internal/ast"
	"github.com/flunderpero/metall/metallc/internal/base"
	"github.com/flunderpero/metall/metallc/internal/types"
)

// GenTypeScript returns a self-contained TypeScript module that inlines the
// WASM harness, declares a typed API for every `export` in the main module,
// and exposes a `loadMetall` function that compiles + instantiates a given
// `.wasm` and returns the typed wrappers.
func GenTypeScript(a *ast.AST, module ast.Module, exports []types.ExportWork) string {
	var sb strings.Builder
	fmt.Fprintf(&sb, "// Generated by metallc from %s.\n", filepath.Base(module.FileName))
	sb.WriteString("// Do not edit.\n\n")
	sb.WriteString(WasmHarnessTS())
	sb.WriteString("\n// >>> Generated bindings.\n\n")
	sb.WriteString("export type MetallAPI = {\n")
	for _, exp := range exports {
		funType := base.Cast[types.FunType](exp.Env.Type(exp.FunTypeID).Kind)
		targetFun := base.Cast[ast.Fun](a.Node(exp.FunNodeID).Kind)
		fmt.Fprintf(&sb, "    %s(%s): %s\n",
			exp.CName, tsParamList(a, exp.Env, funType, targetFun), tsReturnType(exp.Env, funType.Return))
	}
	sb.WriteString("}\n\n")
	sb.WriteString("export async function loadMetall(src: Source, options: RunOptions = {}): Promise<MetallAPI> {\n")
	sb.WriteString("    const {instance} = await instantiateMetall(src, options)\n")
	sb.WriteString("    const e = instance.exports as Record<string, Function>\n")
	sb.WriteString("    return {\n")
	for _, exp := range exports {
		funType := base.Cast[types.FunType](exp.Env.Type(exp.FunTypeID).Kind)
		targetFun := base.Cast[ast.Fun](a.Node(exp.FunNodeID).Kind)
		fmt.Fprintf(&sb, "        %s: %s,\n",
			exp.CName, tsWrapper(a, exp, funType, targetFun))
	}
	sb.WriteString("    }\n")
	sb.WriteString("}\n")
	return sb.String()
}

func tsParamList(a *ast.AST, env *types.TypeEnv, funType types.FunType, targetFun ast.Fun) string {
	var sb strings.Builder
	for i, paramTypeID := range funType.Params {
		if i > 0 {
			sb.WriteString(", ")
		}
		name := base.Cast[ast.FunParam](a.Node(targetFun.Params[i]).Kind).Name.Name
		fmt.Fprintf(&sb, "%s: %s", name, tsParamType(env, paramTypeID))
	}
	return sb.String()
}

func tsWrapper(a *ast.AST, exp types.ExportWork, funType types.FunType, targetFun ast.Fun) string {
	argNames := make([]string, 0, len(funType.Params))
	for i := range funType.Params {
		name := base.Cast[ast.FunParam](a.Node(targetFun.Params[i]).Kind).Name.Name
		argNames = append(argNames, name)
	}
	params := strings.Join(argNames, ", ")
	args := strings.Join(argNames, ", ")
	call := fmt.Sprintf("e.%s(%s)", exp.CName, args)
	if isBoolType(exp.Env, funType.Return) {
		call = fmt.Sprintf("Boolean(%s)", call)
	}
	if _, isVoid := exp.Env.Type(funType.Return).Kind.(types.VoidType); isVoid {
		return fmt.Sprintf("(%s) => { %s }", params, call)
	}
	return fmt.Sprintf("(%s) => %s", params, call)
}

func tsParamType(env *types.TypeEnv, typeID types.TypeID) string {
	switch kind := env.Type(typeID).Kind.(type) {
	case types.BoolType:
		return "boolean"
	case types.IntType:
		return tsIntType(kind)
	}
	panic(base.Errorf("tsParamType: %s is not TS-exportable", env.TypeDisplay(typeID)))
}

func tsReturnType(env *types.TypeEnv, typeID types.TypeID) string {
	if _, ok := env.Type(typeID).Kind.(types.VoidType); ok {
		return "void"
	}
	return tsParamType(env, typeID)
}

func tsIntType(t types.IntType) string {
	if t.Bits >= 64 {
		return "bigint"
	}
	return "number"
}

func isBoolType(env *types.TypeEnv, typeID types.TypeID) bool {
	_, ok := env.Type(typeID).Kind.(types.BoolType)
	return ok
}
